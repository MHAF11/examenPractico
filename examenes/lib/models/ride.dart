class Ride {
  final String from;
  final String to;
  final DateTime date;
  final String departureTime;
  final String arrivalTime;
  final int passengers;

  Ride({
    required this.from,
    required this.to,
    required this.date,
    required this.departureTime,
    required this.arrivalTime,
    required this.passengers,
  });
}
